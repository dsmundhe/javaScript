//for loop  : - use when we know how many repeations are require

for (let i = 0; i < 5; i++) {
    console.log('Dipak');
}

//while loop : use when we don't know how many repeations is required;

let num = 0;
while (num != 10) {
    console.log('Dipak...');
    num++;
}

//do while loop : use when we don't know how many time loope should run but we want loop run at least once

let num1 = 9;
do {
    console.log('Dipak Mundhe');
    num1++;
} while (num1 != 10);