//methods in array

// push method to add element at the end
let arr = [10, 20, 30, 40, 50, 60];
console.log(arr);
arr.push(0);
console.log(arr);

// pop method to remove last element
arr.pop();
console.log(arr);


//unshift to add element at the start
arr.unshift(500);
console.log(arr);


//shift to delete first elemetn
arr.shift(10);
console.log(arr);

//reverse to reverse an array
arr.reverse();
console.log(arr);